/* Display the front screen with icons */
void OLED::displayFrontScreen (uint16_t moisture, float temperature, float humidity) {
    clearDisplay();

    /* Draw the waterDrop*/
    drawBitmap(ICON_WATERDROP_XPOS, ICON_WATERDROP_YPOS, WaterDrop, ICON_WATERDROP_W,
               ICON_WATERDROP_H, WHITE);

    /* Draw the Soil Moisture bar */
    moisture = round(moisture / 10.0) * 10;

    float fMoisture = static_cast<float>(moisture) / static_cast<float>(100);
    int barTam = ceil(SOIL_BAR_W * fMoisture);

    drawRoundRect(SOIL_BAR_XPOS, SOIL_BAR_YPOS, SOIL_BAR_W , SOIL_BAR_H, SOIL_BAR_RADIUS, WHITE);
    fillRoundRect(SOIL_BAR_XPOS, SOIL_BAR_YPOS, barTam, SOIL_BAR_H, SOIL_BAR_RADIUS, WHITE);

    if (moisture >= 70) {
        drawBitmap(SOIL_BAR_XPOS + 10, SOIL_BAR_YPOS + 3, HappyFace, 14, 15, BLACK);
    }

    /* Draw temperature */
    setTextSize(2);
    setCursor(TEMPERATURE_DATA_XPOS, DATA_YPOS);

    int temperatureInt = round(temperature);
    if (temperatureInt < 10) {
        print(" ");
        print(temperatureInt);
    }
    else if (temperatureInt > 99) {
        print("--");
    }
    else {
        print(temperatureInt);
    }

    setCursor(CELSIUS_XPOS, DATA_YPOS);
    print("C");
    drawCircle(CELSIUS_CIRCLE_XPOS, DATA_YPOS + CELSIUS_RADIUS, CELSIUS_RADIUS, WHITE);

    /* Draw humidity */
    setCursor(HUMIDITY_DATA_XPOS, DATA_YPOS);
    setTextSize(2);

    int humidityInt = round(humidity);
    if (humidity < 10) {
        print(" ");
        print(humidityInt);
    }
    else if (humidity > 99) {
        print("--");
    }
    else {
        print(humidityInt);
    }

    setCursor(PERCENTAGE_XPOS, DATA_YPOS);
    print("%");

    drawBitmap(HUM_ICON_XPOS, DATA_YPOS, HumIcon, HUM_ICON_W, HUM_ICON_H, WHITE);

    display();
}

